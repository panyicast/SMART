    language_changed = QtCore.Signal(str)

    def __init__(self, language: str = "zh") -> None:
        super().__init__()
        self._language = "zh"

    @property
    def language(self) -> str:
        return "zh"

    def t(self, key: str, **kwargs: object) -> str:
        template = TRANSLATIONS["zh"].get(key, key)
        if not kwargs:
            return template
        try:
            return template.format(**kwargs)
        except KeyError:
            return template
