SUPPORTED_LANGUAGES: tuple[str, ...] = ("en", "zh")


class I18nManager(QtCore.QObject):
    language_changed = QtCore.Signal(str)

    def __init__(self, language: str = "en") -> None:
        super().__init__()
        self._language = language if language in SUPPORTED_LANGUAGES else "en"

    @property
    def language(self) -> str:
        return self._language

    def set_language(self, language: str) -> None:
        if language not in SUPPORTED_LANGUAGES:
            return
        if language == self._language:
            return
        self._language = language
        self.language_changed.emit(language)

    def t(self, key: str, **kwargs: object) -> str:
        template = TRANSLATIONS.get(self._language, {}).get(key)
        if template is None:
            template = TRANSLATIONS["en"].get(key, key)
        if not kwargs:
            return template
        try:
            return template.format(**kwargs)
        except KeyError:
            return template
